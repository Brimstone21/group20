sprites.onOverlap(SpriteKind.Projectile, SpriteKind.Enemy, function (sprite, otherSprite) {
    if (projectile.overlapsWith(mySprite)) {
        mySprite.destroy()
        projectile.destroy()
    } else if (projectile.overlapsWith(mySprite2)) {
        mySprite2.destroy()
        projectile.destroy()
    } else if (projectile.overlapsWith(mySprite3)) {
        mySprite3.destroy()
        projectile.destroy()
    }
    info.changeScoreBy(1)
    enemies()
})
sprites.onOverlap(SpriteKind.Player, SpriteKind.Enemy, function (sprite, otherSprite) {
    if (info.life() == 3) {
        mySprite.destroy()
    } else if (info.life() == 2) {
        mySprite2.destroy()
    } else if (info.life() == 1) {
        mySprite3.destroy()
    }
    info.changeLifeBy(-1)
    info.changeScoreBy(-1)
    enemies()
})
